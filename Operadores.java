package br.com.fiap;

public class Operadores {
    public static void main(String[] args) {
        int numero1 = 300;
        int numero2 = 600;
        int soma = numero1 + numero2;
        System.out.println("O valor total desta soma é:\n" + soma);

        int numero3 = 70;
        int numero4 = 30;
        int subtracao = numero3 - numero4;
        System.out.println("O valor total desta subtração é:\n" + subtracao);

        int numero5 = 400;
        int numero6 = 800;
        int multiplicacao = numero5 * numero6;
        System.out.println("O valor total desta multiplicação é:\n" + multiplicacao);

        int numero7 = 800;
        int numero8 = 200;
        int divisao = numero7 / numero8;
        System.out.println("O valor total desta divisão é:\n" + divisao);

    }
}
